void ledCtrlStrt(void *argument)
{
	while(1) {
		osSemaphoreAcquire(adcSemaphoreHandle, osWaitForever);

		for (int i = 0; i < BUFF_SIZE; i++) {
			uint32_t val = (adcBuff[i] * 257) / 4095;
			__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, val);
		}

		HAL_ADC_Start_DMA(&hadc1, adcBuff, BUFF_SIZE);

		osDelay(1);
	}
}
